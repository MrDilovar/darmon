    {
        $order = Order::findOrFail($id);
        $order->data = $order->created_at->format('d-m-y / H:i');
        $statuses = OrderStatus::all();

        return view('admin.order.show', ['order'=>$order, 'statuses'=>$statuses, 'PATH_TO_PRODUCT_IMG'=>self::PATH_TO_PRODUCT_IMG]);
